package ejercicio12;

public class Artista {
    private int idArtista;
    private String artista;

    public Artista(int idArtista, String artista) {
        this.idArtista = idArtista;
        this.artista = artista;
    }

    public String getArtista() {
        return artista;
    }

    public int getIdArtista() {
        return idArtista;
    }

}
