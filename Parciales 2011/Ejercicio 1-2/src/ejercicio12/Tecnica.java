package ejercicio12;

public class Tecnica {
    private int idTecnica;
    private String tecnica;

        public Tecnica(int idTecnica, String tecnica) {
        this.idTecnica = idTecnica;
        this.tecnica = tecnica;
    }

    public int getIdTecnica() {
        return idTecnica;
    }

    public String getTecnica() {
        return tecnica;
    }

}
